module.exports = { 

    /**
     * Sum of two numbers
     * 
     * @example 
     * 
     * n1 = 1, n2 = 3 => result 4
     * 
     * @param {*} n1 First digit 
     * 
     * @param {*} n2 Second digit
     * 
     * @returns 
     */
    sum: function(n1, n2){
        return n1+n2;
    },

    /**
     * subtraction of two numbers
     * @example 
     * n1 = 2, n2 = 1 => result 1
     * 
     * @param {*} n1 First digit 
     * 
     * @param {*} n2 Second digit
     * 
     * @returns 
     */
    subtraction: function(n1, n2){
        return n1-n2;
    },

    /**
     * Multiplication of two numbers
     * @example 
     * n1 = 1, n2 = 3 => result 3
     * 
     * @param {*} n1 First digit 
     * 
     * @param {*} n2 Second digit
     * 
     * @returns 
     */
    multi: function (n1, n2) {
        return n1*n2;
    },


    /**
     * Divition two numbers
     * @example 
     * n1 = 1, n2 = 3 => result 3
     * 
     * @param {*} n1 First digit 
     * 
     * @param {*} n2 Second digit
     * 
     * @returns 
     */
    divition: function (n1, n2) {
        return n1/n2;
    }

 }